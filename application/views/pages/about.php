            <div class="row">
                <div class="col-md-12">
                    <h3 class="grid-heading">Philosophy</h3>
                    <p>
                        <a href="<?=base_url() ?>">The Community Outreach Program (COP)</a>
                         believes in the dignity of man and the development of his potentials to the optimum. The Program further 
                         believes that such development could be attained through the involvement of socially conscious student, 
                         faculty members and non-teaching staff in community services.</p>
                </div>
            </div> <!-- /.row -->
            <hr>
            <div class="row">
                <div class="col-md-12">
                    <h3 class="grid-heading">Vision</h3>
                    <p>
                        The UPHSD Community Outreach Program (COP) is a dynamic, facilitative and integrative office that assists 
                        people to become physically and mentally healthy, especially those in the depressed communities in the 
                        city of Las Piñas and its environs.
                </div>
            </div> <!-- /.row -->
            <hr>
            <div class="row">
                <div class="col-md-12">
                    <h3 class="grid-heading">Mission</h3>
                    <p>
                        To effect an environment supportive of the development of self-reliant, healthy and socially responsible 
                        citizens of Las Piñas, in particular, and the Philippines, in general. </p>
                </div>
            </div>
            <hr>
            <div class="row">
                <div class="col-md-12">
                    <h3 class="grid-heading">Objectives</h3>
                    <p>
                        <strong>General Objectives, The UPHSD Community Outreach Program is envisioned to:</strong>
                        <ul style="list-style-type: decimal; margin-left: 1.5em;">
                            <li>Serve as tool for enhancing projects /activities to realize University goals and objectives in the development of partner communities.</li>
                            <li>Promote a continuing advocacy program to further refine, enrich and expand project strategies and activities that effectively promote and respond to the needs of the marginalized people in the community</li>
                            <li>Facilitate the advocacy for mainstreaming marginalized group the concerned in all development programs, projects and activities under COP umbrella.</li>
                            <li>Facilitate the involvement of the faculty, students, parents and non-teaching personnel with various government agencies, private sector and the community socio-economic resources in the delivery of the basic services to the constituents of the partner communities.</li>
                            <li>Perpetuate the advocacy of the being of service to the inmates of the Bilibid Extension School in the delivery of quality education and conferment of academic degrees to inmate-students.</li>
                            <li>Link and synergize with government organizations and non government organizations as co-benefactors in building sustainable partner communities.</li>
                        </ul>
                    </p><br>
                    <p>
                        <strong>Quality Objectives</strong>
                        <ul style="list-style-type: decimal; margin-left: 1.5em;">
                            <li>Man the unit with adequate and qualified personnel</li>
                            <li>Monitor and evaluate COP activities</li>
                            <li>Create criteria to identify sustainability of the partner community</li>
                            <li>Make use of feedback mechanism to identify the impact of COP activities among partner communities</li>
                            <li>Conduct Needs Assessment Survey prior to implementation of COP activities</li>
                            <li>Implement COP activities that are useful and beneficial to the community being served</li>
                        </ul>
                    </p><br>
                    <p>
                        <strong>Specific Objectives</strong>
                        <ul style="list-style-type: decimal; margin-left: 1.5em;">
                            <li>Facilitate extension services of UPHSD faculty, students and non-teaching to the Las Piñas communities and their outlaying towns through the various colleges of the University, thereby maximizing resources and optimizing the impact on their adapted partner community/s</li>
                            <li>Increase access to social welfare services of the targeted beneficiaries en route to greater synergism and social embeddedness</li>
                            <li>Establish support facilities to increase opportunities for gainful employment and help augment other disabled</li>
                            <li>Provide avenues for life skills development to heads of families, women, out-of-school youth and the disabled</li>
                            <li>Institute mechanism to determine whether the beneficiaries of the services provided by the UPHSD COP actually benefited from the programs</li>
                        </ul>
                    </p>
                </div>
